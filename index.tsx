// Helper to parse dates like "2026年1月20日" to "2026-01-20"
const parseDate = (dateStr: string): string => {
  return dateStr.replace(/(\d+)年(\d+)月(\d+)日/, (_, y, m, d) => 
    `${y}-${m.padStart(2, '0')}-${d.padStart(2, '0')}`
  );
};

// Helper to parse "25.00 %" to 0.25 (decimal)
const parsePercent = (pctStr: string): number => {
  if (!pctStr) return 0;
  // Remove " %" and convert to number, then divide by 100 to get decimal
  const num = parseFloat(pctStr.replace(/[^\d.]/g, ''));
  return isNaN(num) ? 0 : num / 100; 
};

const parseData = (): Task[] => {
  const lines = RAW_DATA_STRING.trim().split('\n').filter(line => 
    line.includes('|') && 
    !line.includes('岗位') &&
    !line.includes('----') // Exclude markdown separator line
  );
  return lines.map((line, index) => {
    const parts = line.split('|').map(p => p.trim());
    return {
      id: String(index),
      post: parts[1],
      personName: parts[2],
      status: parts[3] as Status,
      investmentPct: parsePercent(parts[4]),
      projectName: parts[5],
      requestingDept: parts[6],
      startTime: parseDate(parts[7]),
      endTime: parseDate(parts[8])
    };
  });
};

const parseWorkHours = (): WorkHourEntry[] => {
  const lines = WORK_HOURS_RAW.trim().split('\n').filter(line => 
    line.includes('|') && 
    !line.includes('月份') && // Exclude header
    !line.includes('----')
  );
  return lines.map(line => {
    const parts = line.split('|').map(p => p.trim());
    return {
      month: parts[1],
      deptName: parts[2].replace('大数据中心/', ''), // Clean up dept name for display
      avgHours: parseFloat(parts[3])
    };
  });
};

const parseOvertime = (): Record<string, number> => {
   const lines = OVERTIME_RAW.trim().split('\n').filter(line => line.includes('|') && !line.includes('岗位') && !line.includes('---'));
   const map: Record<string, number> = {};
   lines.forEach(line => {
      const parts = line.split('|').map(p => p.trim());
      if (parts.length >= 2) {
         map[parts[1]] = parseFloat(parts[2]);
      }
   });
   return map;
}

