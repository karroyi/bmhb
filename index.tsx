const OrgProjectView = ({ tasks }: { tasks: Task[] }) => {
  const deptMetrics = useMemo(() => {
    // Group by Dept -> Project
    const deptMap = new Map<string, Map<string, Task[]>>();

    tasks.forEach(task => {
      const dept = task.requestingDept || '未知处室';
      const project = task.projectName || '未知项目';

      if (!deptMap.has(dept)) {
        deptMap.set(dept, new Map());
      }
      const projectMap = deptMap.get(dept)!;
      if (!projectMap.has(project)) {
        projectMap.set(project, []);
      }
      projectMap.get(project)!.push(task);
    });

    const result: DeptGroup[] = [];

    deptMap.forEach((projects, deptName) => {
      const projectMetricsList: ProjectMetrics[] = [];
      
      projects.forEach((projectTasks, projectName) => {
        // Calculate metrics per project
        const postMap = new Map<string, ProjectPostMetric>();
        let totalInProgress = 0;
        let totalPending = 0;
        let totalInv = 0;

        projectTasks.forEach(t => {
          if (!postMap.has(t.post)) {
            postMap.set(t.post, { post: t.post, totalInvestment: 0, inProgressInvestment: 0, pendingInvestment: 0 });
          }
          const pm = postMap.get(t.post)!;
          pm.totalInvestment += t.investmentPct;
          totalInv += t.investmentPct;

          if (t.status === '进行中') {
            pm.inProgressInvestment += t.investmentPct;
            totalInProgress += t.investmentPct;
          } else if (t.status === '待开始') {
            pm.pendingInvestment += t.investmentPct;
            totalPending += t.investmentPct;
          }
        });

        projectMetricsList.push({
          projectName,
          requestingDept: deptName,
          postMetrics: Array.from(postMap.values()),
          totalInvestment: totalInv,
          statusSummary: {
            inProgress: totalInProgress,
            pending: totalPending
          }
        });
      });

      // Sort projects by total investment
      projectMetricsList.sort((a, b) => b.totalInvestment - a.totalInvestment);

      result.push({
        deptName,
        projects: projectMetricsList
      });
    });

    // Sort departments by total projects or total investment
    return result.sort((a, b) => b.projects.length - a.projects.length);
  }, [tasks]);

  return (
    <div className="space-y-4 p-2 animate-in fade-in duration-500 max-w-[1800px] mx-auto">
      <OrgSummarySection tasks={tasks} />
      {deptMetrics.map((dept) => (
        <div key={dept.deptName} className="flex gap-2 items-center">
          
          {/* Left Column: Root Node */}
          <div className="w-32 shrink-0 flex flex-col justify-center relative">
              <div className="bg-slate-800 text-white p-2 rounded-lg z-10 relative shadow-md border border-slate-700 hover:scale-105 transition-transform duration-300">
                  <div className="text-[9px] text-slate-400 font-bold uppercase tracking-wider mb-0.5">需求提出处室</div>
                  <h2 className="text-xs font-bold leading-tight">{dept.deptName}</h2>
                  <div className="mt-1 pt-1 border-t border-slate-700 flex justify-between items-center text-[9px] text-slate-300">
                    <span className="flex items-center gap-1"><Network className="w-2.5 h-2.5" /> {dept.projects.length}</span>
                  </div>
              </div>
              {/* Connector from Dept to Grid Spine */}
              <div className="absolute right-0 top-1/2 w-2 h-px bg-slate-300 translate-x-2"></div>
          </div>

          {/* Right Column: Project Grid */}
          <div className="flex-1 relative pl-2 border-l-2 border-slate-200 py-1">
              <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 2xl:grid-cols-4 gap-2">
                {dept.projects.map((proj) => (
                    <div key={proj.projectName} className="relative group/item bg-white p-2 rounded shadow-sm border border-slate-200 hover:shadow-md hover:border-red-200 transition-all duration-200">
                        {/* Header: Title + Total */}
                        <div className="flex justify-between items-start mb-2 gap-2 border-b border-slate-50 pb-1">
                            <h3 className="font-bold text-slate-800 text-xs truncate leading-tight" title={proj.projectName}>
                                {proj.projectName}
                            </h3>
                            <div className="flex items-center gap-0.5 text-[10px] font-black bg-slate-100 px-1.5 py-0.5 rounded shrink-0">
                                <span className="text-green-600">{proj.statusSummary.inProgress.toFixed(2)}</span>
                                <span className="text-slate-400">+</span>
                                <span className="text-red-600">{proj.statusSummary.pending.toFixed(2)}</span>
                            </div>
                        </div>

                        {/* Post Breakdown - Vertical Layout */}
                        <div className="flex flex-col gap-2">
                            {proj.postMetrics.map(pm => {
                                // const maxPostInv = Math.max(...proj.postMetrics.map(p => p.totalInvestment));
                                return (
                                    <div key={pm.post} className="flex flex-col w-full">
                                        <div className="flex justify-between items-end text-[10px] leading-none mb-1">
                                            <span className="text-slate-600 font-medium truncate max-w-[80px]" title={pm.post}>{pm.post}</span>
                                            <div className="flex items-center gap-0.5 font-bold">
                                                <span className="text-green-600">{pm.inProgressInvestment.toFixed(2)}</span>
                                                <span className="text-slate-400 text-[8px]">+</span>
                                                <span className="text-red-600">{pm.pendingInvestment.toFixed(2)}</span>
                                            </div>
                                        </div>
                                        <div className="h-1.5 bg-slate-50 rounded-full overflow-hidden flex w-full border border-slate-100">
                                            <div style={{ width: `${(pm.inProgressInvestment / (pm.totalInvestment || 1)) * 100}%` }} className="bg-green-500 h-full"></div>
                                            <div style={{ width: `${(pm.pendingInvestment / (pm.totalInvestment || 1)) * 100}%` }} className="bg-red-500 h-full"></div>
                                        </div>
                                    </div>
                                )
                            })}
                        </div>
                    </div>
                ))}
              </div>
          </div>
        </div>
      ))}
    </div>
  );
};

