// Metrics for Page 1
interface PersonMetrics {
  personName: string;
  post: string;
  inProgressCount: number;
  backlogCount: number;
  projectCount: number;
}

interface PostGroup {
  post: string;
  avgProjectCount: number;
  totalPeopleCount: number;
  topPeople: PersonMetrics[];
}

// Metrics for Page 2
interface ProjectPostMetric {
  post: string;
  totalInvestment: number; // sum of decimals
  inProgressInvestment: number;
  pendingInvestment: number;
}

interface ProjectMetrics {
  projectName: string;
  requestingDept: string;
  postMetrics: ProjectPostMetric[];
  totalInvestment: number;
  statusSummary: {
    inProgress: number; // total decimal
    pending: number;    // total decimal
  };
}

interface DeptGroup {
  deptName: string;
  projects: ProjectMetrics[];
}

